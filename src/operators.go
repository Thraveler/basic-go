package main

import "fmt"

func main() {
	x := 10
	y := 25

	// addition
	fmt.Println(x + y) // 35

	// substraction
	fmt.Println(x - y) // -15

	// multiplication
	fmt.Println(x * y) // 250

	// division
	fmt.Println(x / y) // .4
	
	// module
	fmt.Println(x % y) // 0

	// incremental
	x++
	fmt.Println(x) // 11

	// decremental
	y--
	fmt.Println(y) // 24
	

}